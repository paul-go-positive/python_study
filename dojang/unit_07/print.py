# [ sep : put delimiter beside each arguments ]
print('2022','03','13',sep='-') # 2022-03-13

# special character

# [ \n : line break ]
print('line break\nline break')
#line break
#line break

# [ \t : tab ]
print('tab\ttab')
# tab   tab

# [ \\ : print back slash ]
print('back slash = \\')
# back slash = \


# [ end : put custome end character ( default = \n ) ]
print('hello') # hello\n
print('hello',end='.') # hello.